package Extd;

public class Shape {

    private float a;
    private float b;

    public Shape(float b, float a) {
        this.b = b;
        this.a = a;
    }

    public float Perimeter() {
        return 0;
    }

    public float getB() {
        return b;
    }

    public void setB(float b) {
        this.b = b;
    }

    public float getA() {
        return a;
    }

    public void setA(float a) {
        this.a = a;
    }
}
